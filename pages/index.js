import React, { useEffect, useRef, useState } from "react";
import { Canvas, useFrame } from "@react-three/fiber";
import { useControls } from "leva";

import Head from "next/head";
import Image from "next/image";
import styles from "../styles/Home.module.css";

export default function Home() {
  function Box(props) {
    // This reference will give us direct access to the mesh
    const mesh = useRef();
    console.log(mesh);

    let { x, y, z, color } = useControls({ x: 1, y: 1, z: 1, color: "#11aaff" });

    // Set up state for the hovered and active state
    const [hovered, setHover] = useState(false);
    const [active, setActive] = useState(false);
    // Subscribe this component to the render-loop, rotate the mesh every frame
    useFrame((state, delta) => (mesh.current.rotation.x += 0.01));
    // Return view, these are regular three.js elements expressed in JSX
    return (
      <mesh {...props} ref={mesh} scale={active ? 1.5 : 1} onClick={(event) => setActive(!active)} onPointerOver={(event) => setHover(true)} onPointerOut={(event) => setHover(false)}>
        <boxGeometry args={[x, y, z]} />
        <meshStandardMaterial color={hovered ? "hotpink" : color} />
      </mesh>
    );
  }

  return (
    <div className={styles.container}>
      <Head>
        <title>Three experiments</title>
        <meta name="description" content="Generated by create next app" />
        <link rel="icon" href="/favicon.ico" />
      </Head>

      <Canvas flat="false">
        <ambientLight />
        <pointLight position={[10, 10, 10]} />
        <Box position={[-1.2, 0, 0]} />
        <Box position={[1.2, 0, 0]} />
      </Canvas>
    </div>
  );
}
